def data_types():
    e = 10  # integer
    print(e)
    y = 10.5  # double
    print(y)
    z = float(10.45)  # float
    print(z)
    a = True  # True/ False
    print(a)
    b = 'y'  # character
    print(b)
    c = 'sample'  # string
    print(c)
    d = 'sample\'s'  # escape sequence
    print(d)


if __name__ == '__main__':
    data_types()
